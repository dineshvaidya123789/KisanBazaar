// --- Helper Functions ---

/**
 * Get list of available states
 */
export const getStates = () => Object.keys(locationData).sort();

/**
 * Get list of districts for a given state
 */
export const getDistricts = (stateName) => {
    if (!stateName || !locationData[stateName]) return [];
    return Object.keys(locationData[stateName]).sort();
};

/**
 * Get full district object (has label & tehsils)
 */
export const getDistrictData = (stateName, districtName) => {
    if (!stateName || !districtName || !locationData[stateName] || !locationData[stateName][districtName]) {
        return null;
    }
    return locationData[stateName][districtName];
};

/**
 * Get list of tehsils for a given district
 */
export const getTehsils = (stateName, districtName) => {
    const data = getDistrictData(stateName, districtName);
    return data ? data.tehsils.sort() : [];
};

// Helper to get ALL districts (flattened) for search
export const getAllDistricts = () => {
    let all = [];
    Object.values(locationData).forEach(districtsMap => {
        all = [...all, ...Object.keys(districtsMap)];
    });
    return [...new Set(all)].sort();
};

// Helper to get ALL tehsils (flattened) for search
export const getAllTehsils = () => {
    let all = [];
    Object.values(locationData).forEach(districtsMap => {
        Object.values(districtsMap).forEach(d => {
            if (d.tehsils) all = [...all, ...d.tehsils];
        });
    });
    return [...new Set(all)].sort();
};
