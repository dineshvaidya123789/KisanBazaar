/**
 * Get district from city/town name
 * @param {string} cityName - City or town name from Nominatim
 * @returns {string} - Mapped district name or original city name
 */
export function getCityToDistrict(cityName) {
    if (!cityName) return '';
    return cityToDistrictMap[cityName] || cityName;
}
